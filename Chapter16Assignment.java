    void trigono(String k, int tt) {
    	if (nodobt == 0)
    	    return;
    
        if (disp[j]==0) {        	
        	jTextField1.setText(k);
            disp[j]=1;
        }  
    
        else 
            jTextField1.setText(disp[j]+k);
            doit=tt;
            nodobt=0;
            m=k;
            print();
            j++;
            trig=1;
    }//end trigono
   
    void print() {
        if (trig==1) {        	
    	    jTextField1.setText(disp[j-1]+m+disp[j]+"");
        }
        
        else
        jTextField1.setText(disp[j]+"");
    }//end print

    void flush() {    	
        i=0;
        j=0;
        doit=0;
        deci=0;
        jTextField1.setText("0");
        for (int u=0; u<=9; u++) {       	
    	    disp[u]=0;
        }
        nodob=0;
        trig=0;
        m="";
        nodobt=1;
    }//end flush
    
    void middle(int z) {
    	if (nodob == 0) {
            return;
        }
    	
        i=0;
        deci=0;
        nodobt=1;
        doit=z;
        nodob=0;
        j++;
        if (j>9)
            j=0;
    }//end middle
    
    void numbers(int u) {	
        if (deci==1){   	
            i=-i;
            disp[j]=disp[j]+ (double) (u*Math.pow(10.0000, i)*100)/100;
        }
        else
            disp[j]=(disp[j]*10)+u;
            print();
        
        if (deci==1)
            i=-i;
        i++;
        nodob=1;
    }//end numbers
    
