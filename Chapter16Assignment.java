    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {
    	
    	String olay="";
        if (nodob == 0)
        return;
        if (doit==2)//addition
        	 disp[j]=disp[j-1]+disp[j];
                 
        else if (doit==3) //subtraction
        	 disp[j]=disp[j-1]-disp[j];
        
        else if (doit==4) { //division
        	 if (disp[j]==0)
             olay="Cannot display infinty value.";
             disp[j]=disp[j-1]/disp[j];
        }
        
        else if (doit == 5) { //multiplication
            disp[j]=disp[j-1]*disp[j];
        }
        
        else if (doit==6) { //sine        	
        	if (mpn==Math.PI/180 && disp[j]==0) {
        	     trig=0;
        	     jTextField1.setText(disp[j]+olay);
        	     nodobt=1;
        	     return;
            }
            disp[j]=disp[j-1]*Math.sin(disp[j]*mpn);
            trig=0;
        }
        
        else if (doit==7) { //cosine
        	
        	if (mpn==Math.PI/180 && disp[j]==90) {
                disp[j]=0;
                trig=0;
                jTextField1.setText(disp[j]+olay);
                nodobt=1;
                return;
             }
        	
            disp[j]=disp[j-1]*Math.cos(disp[j]*mpn);
            trig=0;
        }
        
        else if (doit==8) { //tan
            if (mpn==Math.PI/180 && disp[j]==90) {
            	trig=0;
                olay=" is bad input. ERROR!";
                jTextField1.setText(disp[j]+olay);
                nodobt=1;
                return;
            }
    
            disp[j]=disp[j-1]*Math.tan(disp[j]*mpn);
            trig=0;
        }
             
    else
    	disp[j]=disp[j];
        jTextField1.setText(disp[j]+olay);
        j++;
        disp[j]=0;
        nodob=0;
        nodobt=1;
        i=0;
    }//end button 15
  
